// Short descriptions (8-14 words Russian, warm tone)
function getShort(ms, js, v) {
  const md = m[ms], jd = j[js];
  if (v === 1) return `когда ${md.d} встречает ${jd.o_acc} — рождается вдохновенное действие`;
  if (v === 2) return `${md.p} под крылом ${jd.f_gen} — сердце выбирает смелость`;
  if (v === 3) return `${md.a} ${so(jd.w_inst)} — напор, направленный смыслом`;
  return `${md.g} в масштабе ${jd.e_gen} — решимость, знающая свой путь`;
}

// Helper: pick "с" or "со" before instrumental
function so(word) {
  const first = word.charAt(0).toLowerCase();
  if ('сзжшщ'.includes(first) && word.length > 1 && !'аеёиоуыэюя'.includes(word.charAt(1))) { return 'со ' + word; }
  return 'с ' + word;
}

// Full descriptions (35-70 words Russian), warm tone, no "будет", no штампы
function getFull(ms, js, v) {
  const md = m[ms], jd = j[js];
  if (v === 1) {
    return `${md.d} соединяется ${so(jd.o_inst)} — и тогда даже самое малое начинание обретает размах, а каждое «да» звучит как обещание приключения. Действие и надежда сплетаются в узор, где смелость не безрассудна, а оптимизм — не наивен; оба качества усиливают друг друга, превращая обычный шаг в прыжок веры.`;
  }
  if (v === 2) {
    return `${md.p} опирается на ${jd.f_acc} — и тогда даже в гуще боя сердце помнит, ради чего стоит сражаться. Внутренний огонь питается смыслом, а размах не растрачивается на пустое. Страсть здесь — не слепая сила, а осознанный выбор, превращающий риск в шаг навстречу себе.`;
  }
  if (v === 3) {
    return `${md.a} обретает перспективу через ${jd.w_acc} — здесь напор становится мудрым, а мировоззрение — деятельным. Марс не слепнет в азарте, Юпитер не застывает в догме: рождается живое, пульсирующее понимание, готовое воплощаться в поступках, а не оставаться красивой идеей.`;
  }
  return `${md.g} разворачивается в пространстве ${jd.e_gen} — и тогда дерзновение обретает зрелость, а экспансия — энергию. Риск перестаёт быть авантюрой, становясь путём; масштаб из мечты превращается в план, где каждый шаг подкреплён убеждённостью и внутренней правдой.`;
}

const results = {};
let count = 0;

for (const ms of marsSigns) {
  for (const js of jupiterSigns) {
    const kpref = `signpair.mars_jupiter.${ms}_${js}`;
    for (let v = 1; v <= 4; v++) {
      results[`${kpref}.${v}`] = {
        id: v,
        key: kpref,
        type: 'sign_pair_combo',
        short: getShort(ms, js, v),
        full: getFull(ms, js, v),
        tags: tagSets[v-1],
        audience: 'general',
        lang: 'ru',
        version: '1.0',
        needs_review: false
      };
      count++;
    }
  }
}

const outDir = path.join(__dirname, '..', 'data', 'generated', 'sign_pair_combos');
fs.mkdirSync(outDir, { recursive: true });
const outPath = path.join(outDir, 'mars_jupiter.json');
fs.writeFileSync(outPath, JSON.stringify(results, null, 2), 'utf-8');

console.log(`Generated ${count} entries`);

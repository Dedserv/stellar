# Short descriptions — 4 variants per pair
function Get-Short($ms, $js, $v) {
  $md = $m[$ms]; $jd = $j[$js]
  if ($v -eq 1) { return "$($md.d) встречает $($jd.o)" }
  if ($v -eq 2) { return "$($md.p) под крылом $($jd.f)" }
  if ($v -eq 3) { return "$($md.a) с $($jd.w)" }
  return "$($md.g) в масштабе $($jd.e)"
}

# Full descriptions — 4 variants per pair, 35-70 words, warm tone, no "будет"
function Get-Full($ms, $js, $v) {
  $md = $m[$ms]; $jd = $j[$js]
  if ($v -eq 1) {
    return "Марсианский $($md.d) соединяется с $($jd.o) — и тогда даже малое начинание обретает размах, а каждое «да» звучит как обещание приключения. Действие и надежда сплетаются в узор, где смелость не безрассудна, а оптимизм не наивен."
  }
  if ($v -eq 2) {
    return "$($md.p) опирается на $($jd.f) — и тогда даже в гуще боя сердце помнит, ради чего стоит сражаться. Внутренний огонь питается смыслом, а размах не растрачивается на пустое, превращая риск в осознанный шаг."
  }
  if ($v -eq 3) {
    return "$($md.a) обретает перспективу через $($jd.w) — здесь напор становится мудрым, а мировоззрение — деятельным. Марс не слепнет в азарте, Юпитер не застывает в догме: рождается живое, пульсирующее понимание, готовое воплощаться."
  }
  return "$($md.g) разворачивается в пространстве $($jd.e) — и тогда дерзновение обретает зрелость, а экспансия — энергию. Риск перестаёт быть авантюрой, становясь путём; масштаб из мечты превращается в план."
}

$results = [ordered]@{}
$count = 0

foreach ($ms in $mars_signs) {
  foreach ($js in $jupiter_signs) {
    $kpref = "signpair.mars_jupiter.$ms" + "_$js"
    for ($v = 1; $v -le 4; $v++) {
      $entry = [ordered]@{
        id          = $v
        key         = $kpref
        type        = 'sign_pair_combo'
        short       = (Get-Short $ms $js $v)
        full        = (Get-Full $ms $js $v)
        tags        = $tag_sets[$v-1]
        audience    = 'general'
        lang        = 'ru'
        version     = '1.0'
        needs_review = $false
      }
      $results["$kpref" + ".$v"] = $entry
      $count++
    }
  }
}

$dir = 'stellar\data\generated\sign_pair_combos'
New-Item -ItemType Directory -Force -Path $dir | Out-Null

$json = $results | ConvertTo-Json -Depth 5
[System.IO.File]::WriteAllText("$PWD\$dir\mars_jupiter.json", $json, [System.Text.UTF8Encoding]::new($false))

Write-Host "Generated $count entries"
Write-Host "File: $PWD\$dir\mars_jupiter.json"

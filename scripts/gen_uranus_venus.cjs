// === FULL generators ===
function genFull(uSign, vSign) {
  const u = pick(uranusDesc[uSign]);
  const v = pick(venusDesc[vSign]);
  const interaction = pick(interactions[vSign] || interactions.aries);
  const challenge = pick(challenges);

  const patterns = [
    // A: Uranus → Venus → interaction → challenge
    () => `${u} ${v} ${interaction} ${challenge}`,
    // B: Venus → Uranus → interaction → challenge
    () => `${v} ${u} ${interaction} ${challenge}`,
    // C: Interaction → Uranus → Venus → challenge
    () => `${interaction} ${u} ${v} ${challenge}`,
    // D: Thematic open → Uranus → Venus → interaction → challenge
    () => {
      const opens = [
        `Два мира сталкиваются: ураническая жажда новизны и венерианская потребность в ${{
          aries:'остроте', taurus:'надёжности', gemini:'разговоре', cancer:'уюте',
          leo:'признании', virgo:'порядке', libra:'гармонии', scorpio:'полноте',
          sagittarius:'просторе', capricorn:'основательности', aquarius:'нестандартности', pisces:'слиянии'
        }[vSign]}.`,
        `Уранический импульс раз за разом пересобирает венерианский ландшафт — знакомые чувства вдруг обретают незнакомые очертания.`,
        `Сложный коктейль: Уран даёт ${pick(['дерзость','отрыв','рывок','прорыв','вольницу'])}, Венера добавляет ${
          {aries:'огонь', taurus:'плоть', gemini:'слова', cancer:'тепло', leo:'свет',
           virgo:'заботу', libra:'стиль', scorpio:'страсть', sagittarius:'ветер',
           capricorn:'стержень', aquarius:'идею', pisces:'таинство'}[vSign]
        }.`,
        `Как только кажется, что всё устаканилось, — новый разряд.`,
      ];
      return `${pick(opens)} ${u} ${v} ${interaction} ${challenge}`;
    },
    // E: Contrast question → answer through description
    () => {
      const questions = [
        `Что происходит, когда бескомпромиссная свобода встречает глубокую привязанность?`,
        `Можно ли быть одновременно бунтарём и романтиком?`,
        `Способна ли душа, требующая простора, выдержать тесноту близости?`,
      ];
      return `${pick(questions)} ${u} ${v} ${interaction} ${challenge}`;
    },
  ];

  let text = safe(pick(patterns)());
  let wc = wordCount(text);

  // Adjust to 50-70 word range
  if (wc < 50) {
    const extras = [
      'Иногда это приводит к блестящим открытиям, иногда — к болезненным, но нужным разрывам.',
      'Ключ — не выбирать между свободой и близостью, а научиться дышать и тем, и другим.',
      'Парадокс в том, что удержать может только тот, кто готов отпустить.',
      'Со временем становится ясно: свобода и любовь не враги, а странные союзники.',
      'Настоящая проверка — не найти идеальный баланс, а выдерживать отсутствие баланса и не разрушаться.',
    ];
    text += ' ' + pick(extras);
  }

  wc = wordCount(text);
  if (wc > 75) {
    const words = text.split(/\s+/);
    // Cut at sentence boundary near 70
    let cutAt = 68;
    // Find nearest sentence end before cutAt
    for (let i = Math.min(cutAt + 10, words.length - 1); i >= cutAt - 5 && i >= 0; i--) {
      if (words[i] && words[i].endsWith('.')) {
        text = words.slice(0, i + 1).join(' ');
        break;
      }
    }
    if (wordCount(text) > 75) {
      text = words.slice(0, 68).join(' ') + '.';
    }
  }

  return safe(text);
}

// === TAGS generator ===
function genTags(uSign, vSign) {
  const uTagMap = {
    aries:'порыв', taurus:'материя', gemini:'интеллект', cancer:'эмоции',
    leo:'самовыражение', virgo:'анализ', libra:'эксперимент', scorpio:'кризис',
    sagittarius:'горизонты', capricorn:'структура', aquarius:'инновация', pisces:'интуиция'
  };

  const uTag = uTagMap[uSign];
  const vTags = venusTags[vSign].slice(0, 2);
  const tags = [uTag, ...vTags];
  // Ensure uniqueness
  return [...new Set(tags)].slice(0, 3);
}

